    private void SixtySecondPointsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SixtySecondPointsActionPerformed
        if(team1_60.isSelected()){
            Team1Score.setText(Integer.toString(Integer.parseInt(Team1Score.getText())+bonusTotal));
        }
        else if (team2_60.isSelected()){
            Team2Score.setText(Integer.toString(Integer.parseInt(Team2Score.getText())+bonusTotal));
        }
        bonusTotal = 0;
        resetToggles();
        timerButton.setEnabled(false);
        sixtySecPtsLabel.setText(Integer.toString(0));
        buttonGroup.clearSelection();
    }//GEN-LAST:event_SixtySecondPointsActionPerformed

    private void timerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_timerButtonActionPerformed
        // TODO add your handling code here:
        if (time == 60){
            time--;
            timer.start();
            timerLabel.setText(""+time);
            timerButton.setText("Reset");
        }
        else {
            time=60;
            timer.stop();
            timerLabel.setText(""+time);
            timerButton.setText("Start");
            //timerButton.setEnabled(false);
        }
    }//GEN-LAST:event_timerButtonActionPerformed


