    private boolean isSweep() {
        for (JToggleButton b : buttonList)
            if (!b.isSelected()) {
                sweepLabel.setVisible(false);
                return false;
            }
        bonusTotal = 120;
        return true;
    }

    private void resetToggles() {

        for(JToggleButton b : buttonList){
            b.setSelected(false);
            b.setEnabled(false);
        }
        sweepLabel.setVisible(false);
        //timerButton.setEnabled(false);
        time = 60;
        timerLabel.setText(""+time);
    }

